package main

import (
	"calendar"
	"calendar/internal/app/handler"
	"calendar/internal/pkg/service"
	"calendar/internal/pkg/storage"
	"github.com/spf13/viper"
	"log"
)

func main() {
	viper.AddConfigPath("configs")
	viper.SetConfigName("config")
	if err := viper.ReadInConfig(); err != nil {
		log.Fatalf("error initializing configs: %s", err.Error())
	}
	db, err := storage.NewDB(storage.Config{
		Host:     viper.GetString("db.host"),
		Port:     viper.GetString("db.port"),
		Username: viper.GetString("db.username"),
		Password: viper.GetString("db.password"),
		DBName:   viper.GetString("db.dbname"),
		SSLMode:  viper.GetString("db.sslmode"),
	})
	if err != nil {
		log.Fatalf("failed to initialize db: %s", err.Error())
	}

	storages := storage.NewCalendarStorage(db)
	services := service.NewService(storages)
	handlers := handler.NewHandler(services)

	srv := new(calendar.Server)
	c := make(chan interface{})
	go func() {
		if err = srv.Run(viper.GetString("port"), handlers.InitRoutes()); err != nil {
			log.Fatalf("error occured running http server: %s", err.Error())
		}
		close(c)
	}()
	<-c
}
